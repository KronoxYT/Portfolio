// COMPONENTE: SectionTitle (Título de Sección)
// 
// PROPÓSITO:
// - Títulos consistentes para todas las secciones
// - Incluye título y subtítulo opcional
// 
// PROPS:
// - title: string - título principal
// - subtitle: string (opcional) - subtítulo descriptivo
// - align: 'left' | 'center' | 'right' - alineación del texto
// - className: clases adicionales
// 
// DISEÑO:
// - Título grande y prominente (h2)
// - Subtítulo más pequeño y sutil
// - Línea decorativa o underline (opcional)
// - Animación de entrada al hacer scroll
// 
// USO:
// <SectionTitle 
//   title="Mis Proyectos" 
//   subtitle="Algunos trabajos que he realizado"
//   align="center"
// />
//
// ESTILOS:
// - Tipografía: fuente bold para título
// - Color: usar variables de tema
// - Espaciado: margen inferior apropiado
