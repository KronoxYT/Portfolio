// COMPONENTE: SkillCard (Tarjeta de Habilidad)
// 
// PROPÓSITO:
// - Card individual para mostrar una tecnología o skill
// - Usado dentro del componente About
// 
// PROPS ESPERADAS:
// - skill: {
//     name: string
//     icon: component/string (icono de la tecnología)
//     level: string (opcional: "Básico", "Intermedio", "Avanzado")
//     color: string (color asociado a la tecnología)
//   }
// 
// SUGERENCIAS DE DISEÑO:
// - Icono grande y centrado
// - Nombre de la tecnología
// - Nivel de dominio (opcional)
// - Fondo con el color de la tecnología (sutil)
// - Hover effect: animación suave
// - Border o shadow que resalte
// 
// ALTERNATIVAS:
// - Solo icono + nombre (minimalista)
// - Con progress bar de nivel
// - Con años de experiencia
//
// ANIMACIÓN:
// - Aparecer con delay progresivo
// - Efecto de brillo al pasar el mouse
